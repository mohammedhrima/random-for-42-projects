int ft_strlen(const char *str)
{
	int i;

	i = 0;
	while (str && str[i])
		i++;
	return (i);
}

int ft_putchar(int c)
{
	int i;

	i = (int)write(1, &c, 1);
	return (i);
}

int ft_putstr(char *str, int n)
{
	int i;
	int j;
	int k;
	int len;

	i = 0;
	j = 0;

	if(n >= 0)
		len = n;
	else
		len = ft_strlen(str);
	if (!str)
		return (j+= ft_putstr("(null)",-1));
	while (i < len && str[i])
	{
		j += ft_putchar(str[i]);
		if (j < 0)
			return (-1);
		i++;
	}

	return (j);
}

char	*ft_strchr(char *str, char to_find)
{
	int i;

	i = 0;
	while (str && str[i])
	{
		if (str[i] == to_find)
			return(str + i);
		i++;
	}
	return (NULL);
}

int ft_putnbr(long int num)
{
	int i;
	int j;

	i = 0;
	if (num < 0)
	{
		j = ft_putchar('-');
		if (j < 0)
			return (-1);
		i += j;
		num = -num;
	}
	if (num >= 0 && num < 10)
	{
		j = ft_putchar('0' + num);
		if (j < 0)
			return (-1);
		i += j;
	}
	if (num >= 10)
	{
		j = ft_putnbr(num / 10);
		if (j < 0)
			return (-1);
		i += j;
		j = ft_putchar(num % 10 + '0');
		if (j < 0)
			return (-1);
		i += j;
	}
	return (i);
}

